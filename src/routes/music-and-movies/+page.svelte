<h1>Music &amp; Movies</h1>

<p>BoldContacts can connect to many popular music services and movie services, including YouTube, Spotify, Apple Music, Amazon Prime Video, Google TV, and more.</p>
    
<img src="/assets/explainers/music-and-movies/@1600x830.png" alt="BoldContacts connects to music and movies" loading="lazy">


<h2>How to link</h2>

<p>Many major services have ways to link directly to an artist, album, song, playlist, video, streaming radio station, or streaming video channel.</p>

<p>The link is called a "URL" which stands for "Uniform Resource Locator". The URL is an internet address, such as a web page address, or a media player content file address.</p>

<p>Each major service has its own way link to content by using a URL. You will need to use whichever way your preferred service uses.</p>


