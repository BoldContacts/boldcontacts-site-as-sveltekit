<h1>Carers &amp; Caregivers</h1>

<p>The BoldContacts app can be especially good for carers and caregivers.</p>

<p>The BoldContacts app enables your caree to make phone calls and video calls more easily. You can help your caree with installing the app, and setting up the contacts the way you want and your caree wants.</p>

<ul>

    <li>For example, you can add yourself as a contact, and add your own photo.</li>

    <li>For example, you can manage your career's contacts, to update them with new information, or delete expired information.</li>

</ul>

<p>The app is typically easy to set up on your caree's device.</p>

<ul>

    <li>To get BoldContacts, download it from the Apple App Store <a href="https://apps.apple.com/us/app/boldcontacts/id6443335501">here</a>.</li>

    <li>BoldContacts runs on any current iPhone and iPad, and also runs on many older iPhones and iPads.</li>

</ul>

