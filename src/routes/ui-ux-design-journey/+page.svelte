<h1>UI/UX design journey</h1>
    
<p>BoldContacts has a UI/UX design journey that emphasizes the user's needs.</p>

<p>The UI/UX design journey started when my mother-in-law was diagnosed with Alzheimer's Disease, and my stepfather was diagnosed with Parkinson's Disease, and both parents were diagnosed with eyesight impairments.</p> 

<p>Both parents loved using their iPhones, especially for keeping in touch with friends and relatives via phone calls and FaceTime.</p>

<p>Unfortunately, both parents were having a harder and harder time using their iPhone Contacts app.</p>

<p>To understand why, and to demonstration how BoldContacts came to be developed, it's helpful to start by looking at the typical iPhone Contacts app.</p>


<h2>iPhone Contacts app</h2>

<p>The default iPhone Contacts app list page looks like this:</p>

<p><img src="/assets/ui-ux-design-journey/apple-ios-contacts-list@460x996.png" width="50%" alt="Screenshot of Apple iPhone Contacts app with list view" loading="lazy"></p>


<h2>iPhone Contacts app issues</h2>

<p>Unfortunately, Alzheimer's Disease, Parkinson's Disease, and Age-Related Macular Degeneration (AMD) often cause impairments making it hard to use the default iPhone Contacts app:</p>

<ul>

    <li>Alzheimer's Disease often causes cognitive impairment, including memory issues. My mother-in-law started having trouble remembering how to unlock her iPhone, how to launch the default iPhone Contacts app, and how to connect via phone or FaceTime.</li>

    <li>Parkinson's Disease often causes motor impairment, including hand tremors. My stepfather started having trouble using the iPhone icons, trouble navigating the iPhone Contacts app, and trouble tapping the various buttons and links.</li>

    <li>Age-Related Macular Degeneration often causes vision impairment, including blurred vision and less than 20/20 vision. My folks started having a hard time seeing the iPhone text, even with Apple's accessibility font scaling turned up.</li>

</ul>

<p>As their diseases progressed, they lost their ability to use the default iPhone Contacts app. This loss cut them off from their video calling. This made them sad, lonely, and more confused.</p>

<p>What I learned pretty quickly is that millions of people are affected by cognitive/motor/vision impairments, and many millions more will be affected within the next ten years. The need for a better solution now is very large, and the need for a roadmap to the future is also very large.</p>


<h2>Attempt 1: Look for accessibility apps</h2>

<p>I looked through many Apple App Store apps, to try to find any easier way of making phone calls and video calls.</p>

<p><img src="/assets/ui-ux-design-journey/apple-app-store@960x520.jpg" width="50%" alt="Apple App Store presentation with Apple CEO Tim Cook" loading="lazy"></p>

<p>I found a bunch of potential alternative phone dialer apps and evaluated them:</p>

<ul>
    
    <li>Many of the apps provided more information on each screen, such as for salespeople making many business calls. These apps didn't work for most of the users.</li>

    <li>A few of the apps did provide easier ways to call, but had problems such as recent negative reviews, or a lack of maintenance, or a requirement for buying a new iPhone to run the newest iOS.</li>

    <li>Key learning: Bold big buttons help, and are significantly easier to see and tap than Apple's default buttons.</li>

</ul>

<p>After much searching, I concluded that there wasn't a viable app.</p>


<h2>Attempt 2: Voice control &amp; Amazon Echo Dot</h2>

<p>Because of the dementia, and hand tremors, and macular degeneration, my next attempt was to try voice control, using the Amazon Echo Dot:</p>

<p><img src="/assets/ui-ux-design-journey/amazon-echo-dot@960x563.jpg" width="50%" alt="Amazon Echo Dot product" loading="lazy"></p>

<p>Unfortunately, most of the users weren't able to understand voice control.</p>

<ul>
    
    <li>For example, my mother-in-law had never used voice control before, and didn't understand the overall concept of talking to the room.</li>

    <li>For example, my stepfather had used Amazon Alexa voice control for years to play his music. But he lost the ability to remember how to trigger Alexa.</li>

    <li>Key learning: a physical device with touch is easier to understand than room-wide voice control.</li>

</ul>

<p>After many tries, I concluded that voice control was too confusing for users. At the same time, I saw them both able to reliably dial their land-line push-button phones. So I searched for a physical solution.</p>


<h2>Attempt 3: Dedicated device &amp; Facebook Portal</h2>

<p>Because most of the users were still able to use their physical phones to some degree, I searched for a physical device that worked more like a desktop phone. After talking with many of my friends, including friends from Facebook, I found the Facebook Portal:</p>

<p><img src="/assets/ui-ux-design-journey/facebook-portal-10-inch@960x782.jpg" width="50%" alt="Facebook Portal 10-inch product" loading="lazy"></p>

<p>Unfortunately, the Facebook Portal is quite confusing, even to me.</p>

<ul>
    
    <li>I tried the setup process for more than hour, and hit many UI/UX glitches and one severe bug. Even once I succeeded, the Portal tried to do much more than they needed.</li>

    <li>Because of the physical weight and shape, the Facebook Portal wasn't easy to carry around, nor put in a purse, nor mount to a wheelchair.</li>

    <li>Key learnings: users preferred the larger screen size of the Portal 10" over the smaller screen size of the iPhone, and also sometimes liked using the desktop stand capability.</li>

</ul>

<p>After a week, I returned the Facebook Portal. In my opinion, the Portal is a great concept, but with too many UI/UX problems. I'll be surprised if Facebook continues to sell it.</p>


<h2>Attempt 4: Dedicated demographic &amp; Grandpad</h2>

<p>Because one of the issue correlations is that users are elderly, I searched for an elderly-specific solution. Eventually found the Grandpad, which provides a dedicated tablet, with a dedicated mobile connection, and elderly-specific software:</p>

<p><img src="/assets/ui-ux-design-journey/grandpad@960x700.jpg" width="50%" alt="Grandpad product" loading="lazy"></p>

<p>The Grandpad works well, and the team is highly helpful. Unfortunately, it fell short for users' specific needs:</p>

<ul>
    
    <li>The Grandpad comes with its own mobile phone service, which has a significant monthly cost.</li>

    <li>The Grandpad was confusing to caregivers, because they'd never seen one, and couldn't quickly figure out how to set it up.</li>

    <li>Key learnings: to be adopted by caregivers, it's a must-have to use commodity devices (iPhones, iPads, etc.) and to use free WiFi.</li>
</ul>

<p>The Grandpad was close, but not quite. What I learned is that a great solution needs to be able to use free WiFi, rather than requiring a mobile phone monthly payment. And a great solution needs to be something very easy for caregivers to understand and use.</p>


<h2>BoldContacts sketches</h2>

<p>I started the BoldContacts sketches informed by the default iPhone Contacts app person page, which looks like this:</p>
    
<p><img src="/assets/ui-ux-design-journey/apple-ios-contacts-page@460x996.png"  width="45%" alt="Screenshot of Apple iPhone Contacts app with page view" loading="lazy"></p>

<p>When I started sketching, I tried these steps first:</p>

<ul>

    <li>Drop unneeded information, such as the postal address map section, email address section, birthday section, etc.</li>

    <li>Remove duplicity, such as the page showing a FaceTime icon near the top, plus the "FaceTime" area.</li>

    <li>Don't use links, such as "Send Message" and "Share Contact", because users didn't understand that the links are tappable.</li>

</ul>


<h2>BoldContacts functionality</h2>

<p>Thanks to all the attempts, I had a good idea of what a solution needed:</p>

<ul>

    <li>Bold big text that is especially easy to see, because this helps people with eyesight problems.</li>

    <li>Bold big buttons that are especially easy to tap, because these help users with tremor problems.</li>

    <li>Usable on a typical iPhone and iPad, because these match what caregivers know and use.</li>

    <li>Usable with existing free WiFi, rather than requiring sign up for a new cellular contract.</li>

</ul>


<h2>Iterations for users</h2>

<p>I started coding and iterating with users and caregivers, and learned more about the user needs:</p>

<ul>

    <li>The number of important contacts is small, typically 9 people or fewer. The important contacts were immediate family members, best friends, and some healthcare providers.</li>

    <li>The app must be free, because any kind of payment is too complex for the user.</li>

    <li>For some users, the device needs extra protection, such as a drop-proof spill-proof case.</li>

    <li>Caregivers strongly want options for form factors, such as an option for a tabletop stand, an option for a wall mount akin to a picture frame, and an option for a wheelchair attachment.</li>

</ul>


<h2>Iterations for Apple iOS</h2>

<p>I learned more about Apple iOS, the Swift programming language, and the SwiftUI framework:</p>

<ul>

    <li>Some of the typical Apple mobile UI/UX interactions do not work well for many users, especially invisible interactions. For example, press-drag-scroll is used in the Contacts app for scrolling up and down through the contacts list; this was confusion, and worse, did not function reliably when the user had hand tremors.</li>

    <li>Some of the Apple security restrictions make it hard to use a cloud-based database, but easy to use the user's existing iOS contacts database.</li>

    <li>Some of the current Apple iOS coding practices won't run on older equipment, such as a six-year-old iPhone or iPad, which cannot run the current iOS. But many of the users have these older devices, and cannot afford to buy a new iPhone, or cannot figure out how to set up a new iPhone.</li>

    <li>Many older devices are cheap and easy to procure. In fact, many users had family members who had an older iPhone or iPad that wasn't being used, and could be transferred to the user.</li>

</ul>


<h2>Simpler is better</h2>

<p>I iterated more and learned in many cases, very simple UI/UX tends to works better for the users:</p>

<ul>

    <li>Use one page for the whole app, rather than multiple pages such as a contacts list page plus a contact person page.</li>

    <li>Spread all the buttons far apart, to make it easier for the user to tap the one they want.</li>

    <li>Display the photo as large as possible, because this helps many users identify the person better/faster, and also provides a feeling of delight.</li>

    <li>Interface elements based on black and white tend to be easier to learn than interface elements based on colors, gradients, grays, and the like.</li>

    <li>Alphabetic sort by first name is easier to understand than alphabetic sort by last name.</li>

    <li>Each contact person typically has one preferred contact method, such as always via a phone call, or always via FaceTime.</li>

    <li>Some users have a very strong preference for their native language. Therefore, internationalization and localization are very important.</li>

</ul>


<h2>Success!</h2>

<p>The result of all the attempts, research, iteration, and testing, is BoldContacts version 1:</p>

<p><img src="/assets/ui-ux-design-journey/boldcontacts-alice@460x882.png" width="50%"  alt="Screenshot of BoldContacts app with page view" loading="lazy"></p>

<p>Key results:</p>

<ul>

    <li>The big main photo provides delight and recognition.</li>

    <li>The bold font is easy to see.</li>

    <li>The bold buttons are easy to see and easy to tap.</li>

    <li>The app uses one page layout, rather than multiple page layouts for a contacts list page that drills into a contacts person page. The app's entire functionality is always available on the page, and in addition navigation easier to understand and use.</li>

    <li>Each contact has one preferred way to call, rather than many ways. The one way can be customized by the user or carer, to use a phone call, or FaceTime video, or any custom URL.</li>
    
</ul>

