<h1>Training</h1>
    
<p>The BoldContacts team provides pro-bono training to people. We can train end users, and we can also train carers, such as medical workers, home care helpers, care facility teams, and advocacy groups.</p>

<p>The training is 100% free and provided by volunteers, and takes just 20 minutes.</p>


<h2>Video training</h2>

<p>If you're a group of carers anywhere in the world, we can do video training via Zoom.</p>

<p>To book a training day and time, use the <a href="https://calendly.com/joelparkerhenderson">online calendar</a>.</p>


<h2>In-person training - pilot program</h2>

<p>If you prefer in-person training, then contact us and let us know who you are, where you are, and any special needs that you and your group may have.</p>

<p>To contact us, use our email address <a href="mailto:training@boldcontacts.org">training@boldcontacts.org</a>.</p>

