<h1>Health &amp; Wellness</h1>

<p>Potential health and wellness connections include videos for home exercise, balance training videos, range-of-motion stretching, chair yoga, senior-friendly tai chi videos, impairment-aware activities, occupational therapy guides, medical recovery videos. and the like.</p>

<p>For example, daily exercise and balance training are both proven to help elderly people increase mobility, muscle tone, steadiness, and longevity. These improvements cap help reduce the risk of future injuries, such as from ankle sprains, accidental falls, hip fractures, and spine compressions.</p> 


<h2>How to link to a exercise video</h2>

<p>Many major video services have ways to link directly to an artist, or album, or song, or video, or playlist.</p>

<p>The link is called a "URL" which stands for "Uniform Resource Locator". The link functions as a web address.</p>

<p>Let's use an example of video for "Exercise".</p>

<p>Each video service has its own way link to video by using a URL. You will need to use whichever way your preferred service uses.</p>

<p>Example URL format:</p>

<ul>

    <li>
        <b>YouTube Video: 10 Minute Beginner Chair Exercises for Seniors</b>
        <br>
        <a href="https://www.youtube.com/watch?v=oumzMyqK-2I">https://www.youtube.com/watch?v=oumzMyqK-2I</a>
    </li>

</ul>


<h2>How to create a contact</h2>

<p>When you know the URL link to the video you want, then you can create a new contact:</p>

<ol>

    <li>Start your typical iOS Contacts app.</li>

    <li>Tap the "+" icon to create a new contact.</li>

    <li>Type any name you want, such as "Exercise".</li>

    <li>Upload any photo you want, such as a photo of the video that you download from the internet.</li>

    <li>Add the URL link to your video.</li>

</ol>

Then quit BoldContacts and relaunch it. This tells BoldContacts to refresh your contact list, including your new contact. As an aside, a future release of BoldContacts will add automatic refresh.
