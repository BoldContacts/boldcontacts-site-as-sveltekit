<h1>Worldwide Freedom</h1>

<p>The BoldContacts app is for everyone, worldwide, for free.</p>
    
<p>The app is pro-bono, 100% volunteer, with zero profit motive, and designed to help people freely communicate.</p>


<h2>Languages</h2>

<p>The app provides many worldwide languages. We welcome help improving the phrasings and translations, and adding more languages and examples.</p>

<p>The app currently includes Afrikaans, Amharic, Arabic, Armenian, Assamese, Azerbaijani, Bangla, Basque, Belarusian, Bosnian, Bulgarian, Catalan, Chinese, Corsican, Croatian, Danish, Dutch, English, Esperanto, Finnish, French, Galician, German, Greek, Gujarati, Hawaiian, Hebrew, Hindi, Hmong, Icelandic, Igbo, Indonesian, Irish, Italian, Japanese, Javanese, Kannada, Khmer, Korean, Lao, Latin, Lingala, Lithuanian, Malay, Maori, Marathi, Meiteilon, Nepali, Norwegian, Persian, Polish, Portuguese, Punjabi, Quechua, Russian, Samoan, Sanskrit, Scots Gaelic, Shona, Slovenian, Swahili, Swedish, Tamil, Telegu, Thai, Turkish, Turkmen, Ukrainian, Urdu, Uyghur, Uzbek, Vietnamese, Welsh, Xhosa, Yiddish, Yoruba, Zulu.</p>

<p>Technical specifics:</p>
    
<ul>

    <li>The app provides the language by using "internationalization and localization", abbreviated as "i18n and l10n".</li>

    <li>Translations are thanks to Google Translate and many volunteers.</li>

    <li>The internationalization and localization files are available to read and edit <a href="https://github.com/BoldContacts/boldcontacts-mobile-app-for-apple/tree/main/Shared/Resources">here</a>.</li>

</ul>


<h2>Open Source</h2>

<p>The project is free libre open source software (FLOSS). Anyone in the world can freely read all the source code <a href="https://github.com/BoldContacts/boldcontacts-mobile-app-for-apple">here</a>.</p>

<p>Anyone anywhere is able to create an project issue, such as a new feature request, or a technical question, or a bug report, or a coordinated security disclosure, all <a href="https://github.com/BoldContacts/boldcontacts-mobile-app-for-apple/issues">here</a>.</p>
        
<p>Technical specifics:</p>

<ul>

    <li>The app uses the GNU General Public License (GPL), version 2, or later.</li>

    <li>The license enables anyone worldwide is free to use, modify, and share the software.</li>

    <li>We can also create open source licenses for special needs, such as for specific medical research projects.</li>

</ul>

