<h1>Collaboration opportunities</h1>

<p>The goal is helping people with impairments, such as eyesight issues, cognitive issues, and motor-control issues.</p>
    
<p>The mobile app is a general-purpose contacts tool, and it's able to connect to YouTube videos, Spotify songs, and much more.</p>


<h2>Who can be a collaborator?</h2>

<p>Collaborators can be content providers who want to help people with impairments, such as:</p>

<ul>

    <li>Health: potential connections include senior-oriented exercises, impairment-aware activities, chair yoga, tai chi, balance training, range-of-motion stretching, and the like.</li>

    <li>Entertainment: potential connections include favorite songs, albums, playlists, genres, and streaming radio stations. Potential movie connections include blockbuster movies, family home movies, and streaming TV.</li>

</ul>

<p>Collaborators can be providers of elderly care and impairment care, such as:</p>

<ul>

    <li>Occupational therapy care: potential connections include how to minimize falls and fractures, how to improve daily medication practices, how to help with loneliness and isolation, and how to involve help via friends and family members.</li>

    <li>Care organization leaders: potential connections include how to do outreach to carers who can use BoldContacts, how to do setup and training of new users, and how to do group-oriented activities such as multi-party video conferencing.</li>

</ul>

<p>Collaborators can be technical contributors, such as:</p>

<ul>

    <li>Language translators: each language we use has a language-specific translation file, that starts with typical Google translate results, and is ready for tuning by people who are fluent in the language.</li>

    <li>Software programmers: we have many features to create, with Apple Swift, Android Kotlin, and Svelte JavaScript.</li>

</ul>

<p>This rest of this page will focus on content collaboration opportunities, because it's the most-direct way for the most people to help right now.</p>


<h2>How will the collaboration process work?</h2>

<p>It's very much open to how you want to participate. The hope is to have you provide links to your own content that is always entirely yours. If you wish to be more involved than that, that's great too.</p>
    
<ul>

    <li>If you want to provide one link, such as to your YouTube video: I add it to the list of friendly links. If you want to provide custom link text, or a custom square thumbnail image, then that's good too.</li>

    <li>If you want to provide an elderly-friendly impairment-friendly web page on your own website: that's awesome and I can link to your page.</li>

    <li>If you want something custom, please ask. The BoldContacts app is able to be useful as a toolkit for creating new kinds of functionality, and the BoldContacts website is especially easy to modify and grow.</li>

</ul>



<h2>What are the benefits and incentives for joining this project?</h2>

<p>You get to help people in need.</p>

<p>For content collaborators:</p>

<ul>

    <li>You get to highlight that people with impairments have different kinds of accessibility needs.</li>

    <li>I'm happy to write a testimonial for you, such as for your website, or your social media, or your email newsletter.</li>

</ul>

<p>For healthcare researchers:</p>

<ul>

    <li>If you're a researcher who wants to track usage metrics, such as for formative or summative assessments, then you're able to use your own link metrics. For example, if you're a healthcare provider and you're using a link to your own exercise video on your own YouTube channel, then you're able to use your own YouTube tracking metrics such as plays per week.</li>

    <li>If you're a researcher who is interested in significantly more data, such as tremor tracking for Parkinson's patients to help them with stabilization medication, or location tracking for Alzheimer's patients to help them not get lost, then please <a href="mailto:joel@boldcontacts.org">email</a>. We can connect about your goals, and how to create a custom version for you, using Apple Health and suitable medical software practices.</li>

</ul>



<h2>How will I be compensated and recognized for my contribution?</h2>

<p>Compensation: you're a better person when you help people.</p>

<p>Recognition: you recognize you're a better person when you help people.</p>

<p>Imagine The Beatles song "The End" and the lyric "The love you take is equal to the love you make". That's the compensation and recognition.</p>


<h2>How do you protect the privacy and security of the users?</h2>

<p>The project uses Apple's best-quality practices for privacy and security.</p>

<ul>

    <li>The user's contacts are stored using Apple iOS security, using the user's own contacts, and with all the relevant Apple prompts for access control, privacy, and security.</li>

    <li>BoldContacts does not upload any contacts, does not use an advertising tracking, does not use credit card billing, etc.</li>

</ul>


<h2>How do you protect the privacy and security of collaborators?</h2>

<p>Collaborators alway use their own existing setups, such as their own YouTube channels, their own Spotify accounts, and their own content tools. BoldContacts never has privacy access or security access to these setups.</p>

<ul>

    <li>The collaborator is simply sharing a public web address that links to their own content.</li>

    <li>If the collaborator wants to do much more, such as editing a web page on the BoldContacts website, then everything uses best-practice systems and tools, such as via GitHub.com.</li>

</ul>


<h2>What is involved in the quality and safety of the app?</h2>

<p>The project uses best-quality practices for programming.</p>

<ul>

    <li>The Apple iOS mobile app uses all the best practices by Apple, such as for the Apple App Store, security, stability, accessibility, and the like.</li>

    <li>The Android mobile app will use similar best practices by Google, such as for the Google Play Store, security, stability, accessbility, and the like.</li>

</ul>

<p>The project is fully open source.</p>

<ul>

    <li>Open source means that anyone in the world can read all the source code <a href="https://github.com/BoldContacts/boldcontacts-mobile-app-for-apple">here</a>.</li>

    <li>Anyone anywhere is able to create an project issue, such as a new feature request, or a technical question, or a bug report, or a coordinated security disclosure, all <a href="https://github.com/BoldContacts/boldcontacts-mobile-app-for-apple/issues">here</a>.</li>
        
</ul>
