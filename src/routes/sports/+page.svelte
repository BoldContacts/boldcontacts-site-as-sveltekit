<h1>Sports</h1>

<p>BoldContacts can connect to many popular sports services and game broadcasters, including via YouTube, Spotify, Apple Music, Amazon Prime Video, Google TV, and more.</p>
    
<img src="/assets/explainers/sports-and-games/@1600x830.png" alt="BoldContacts connects to sports and games" loading="lazy">


<h2>How to link</h2>

<p>Many sports services and game broadcasters have ways to link directly to a streaming radio station or streaming video channel.</p>

<p>The link is called a "URL" which stands for "Uniform Resource Locator". The URL is an internet address, such as a web page address, or a media player content file address.</p>

<p>Each service has its own way link to content by using a URL. You will need to use whichever way your preferred service uses.</p>


