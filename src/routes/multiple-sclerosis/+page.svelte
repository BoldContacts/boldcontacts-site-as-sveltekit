<h1>Multiple sclerosis (MS)</h1>

<span data-chatgpt-prompt="Summarize multiple sclerosis (ELI5)">

    <p>Multiple sclerosis (MS) is a disease that affects the nervous system, specifically the brain and spinal cord. It is caused by damage to the protective covering (myelin) that surrounds nerve fibers, which affects the ability of the nerves to communicate with each other. This can lead to a variety of symptoms such as fatigue, numbness or tingling in the limbs, difficulty with coordination and balance, and vision problems. The severity and progression of MS can vary widely among individuals, and there is currently no cure for the disease.</p>

