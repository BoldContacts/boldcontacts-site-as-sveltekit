<h2>How to create a contact</h2>

<p>When you know the URL, then you can create a new contact:</p>

<ol>

    <li>Start your typical iOS Contacts app.</li>

    <li>Tap the "+" icon to create a new contact.</li>

    <li>Type any name you want, such as "Bible audiobook" or "Spiritual services".</li>

    <li>Add any image you want, such as a photo of The Bible or your local church.</li>

    <li>Add the URL link.</li>

</ol>

