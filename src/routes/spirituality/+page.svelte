<h1>Spirituality</h1>

<p>BoldContacts can connect to spiritual offerings, such as weekly religious gatherings, or volunteers leading prayers, or audiobooks.
    
<img src="/assets/explainers/spirituality/@1600x830.png" alt="BoldContacts connects to spirituality services" loading="lazy">


<h2>How to link</h2>

<p>Many spiritual organizations have ways to link to the organization's videos, or a streaming radio station, or a streaming video channel.</p>

<p>The link is called a "URL" which stands for "Uniform Resource Locator". The URL is an internet address, such as a web page address, or a media player content file address.</p>

<p>Each offering has its own way link to content by using a URL. You will need to use whichever way your preferred service uses.</p>


