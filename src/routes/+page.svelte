<h2>What is BoldContacts?</h2>

<p>BoldContacts™ is a mobile app to help you browse your contacts and connect with them. BoldContacts is especially good for people who experience vision disabilities, or cognitive disabilities, or motor disabilities.</p>

<p>The app displays each contact with a big photo, big font, and big buttons.</p>

<p><img src="/assets/pexels/pexels-andrea-piacquadio-3791664/@1200x600.jpg" alt="People using BoldContacts on a phone" loading="lazy"></p>

<p>A phone button means "contact this person now". This button connects you by using your choice of video, or phone, or email, or message, or custom URL. A caregiver can customize this by using the Apple iOS Contacts app.</p>
    
<p>A left-arrow button means "show the previous contact".</p>
        
<p>A right-arrow button means "show the next contact".</p>


<h2>Why is BoldContacts free?</h2>

<p>BoldContacts is free because we want to help as many people as possible. BoldContacts exists because our family members have Alzheimer's Disease and Parkinson's Disease. BoldContacts is built to help them keep in touch with family, friends, and caregivers.</p>

<img src="/assets/pexels/pexels-kampus-production-7551617/@1200x600.jpg" alt="Happy family uses a tablet computer">

<p>We are actively seeking help spreading the word about BoldContacts, so more people can hear about it, and more people can try it.  If you want more information about this, or can help, please contact Joel Parker Henderson at <a href="mailto:joel@boldcontacts.org">joel@boldcontacts.org</a>.</p>


<h2>Setup & Personalization</h2>

<p>The first time you launch BoldContacts, you will see an Apple iOS prompt that asks for you to approve this access. Say yes.</p>

<img src="/assets/pexels/pexels-kampus-production-7477708/@1200x600.jpg" alt="Person using a tablet for video calling" loading="lazy">

<p>You can personalize how your BoldContacts app connects to your contacts. For example, you can choose whether BoldContacts will call a telephone number, or initiate a FaceTime video connection, or connect to any other service of your choice.</p>

<ol>
    
<li>Launch your typical Apple Contacts app. Find the contact that you want to personalize. Tap the edit button.</li>

<li>Create a new contact field, such as for a phone number, or email address, or URL address. Edit the new field label to be "BoldContacts", and the field value to be whatever you want.</li>

<li>The next time you launch BoldContacts, the app will load your contact's new information, and use your contact's new field.</li>

</ol>


<h2>Music &amp; Movies</h2>

<p>The app can connect to music and movies, such as via YouTube, Spotify, Apple Music, and Amazon Prime Video.</p>

<img src="/assets/pexels/pexels-rdne-stock-project-7020687/@1200x600.jpg" alt="Person playing guitar with records and a record player" loading="lazy">

<p>Potential music connections include favorite songs, albums, playlists, genres, and streaming radio stations. Potential movie connections include blockbuster movies on Amazon Prime Video, family home movies that are uploaded to YouTube, and streaming TV stations.</p>


<h2>Health &amp; Wellness</h2>

<p>The app can connect to health and wellness videos, such as for appropriate exercise, occupational therapy, and medical recovery.</p>

<img src="/assets/pexels/pexels-mikhail-nilov-7500699/@1200x600.jpg" alt="People exercising" loading="lazy">

<p>Potential health and wellness connections include senior-oriented exercises, impairment-aware activities, chair yoga, tai chi, balance training, range-of-motion stretching, and the like.</p>


<h2>Medical &amp; Clinical</h2>

<p>The app can connect to medical providers and clinical researchers by using telemedicine conferencing, such as via Zoom, Teams, and Jitsi.</p>

<img src="/assets/pexels/pexels-rdne-stock-project-6129502/@1200x600.jpg" alt="People exercising" loading="lazy">

<p>Potential medical connections include doctors, nurses, and outpatient follow up providers, all of whom may be able to use telemedicine to help support in-person medical care. Some ideas for clinical researchers include laboratory primary investigators who want patients to check in about updates to studies.</p>


