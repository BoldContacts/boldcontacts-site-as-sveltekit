<h1>BoldContacts + The Bookshop By The Sea</h1>

<p>The Bookshop By The Sea shares a free weekly book reading video that is especially for elderly people. You can use BoldContacts to add a contact for The Bookshop By The Sea that launches YouTube and plays the current book reading video.</p>
    
<p>To add the contact:</p>

<ol>

    <li>Start your typical iOS Contacts app.</li>

    <li>Tap the "+" icon to create a new contact.</li>

    <li>Type any name you want, such as "The Bookshop By The Sea".</li>

    <li>Add this photo:<br><a href="https://boldcontacts.org/thebookshopbythesea/contact.jpg">https://boldcontacts.org/thebookshopbythesea/contact.jpg</a></li>

    <li>Add this URL:<br><a href="https://www.youtube.com/watch?v=Z9Y0OvzjSBU">https://www.youtube.com/watch?v=Z9Y0OvzjSBU</a></li>

</ol>

Then quit BoldContacts and relaunch it. This tells BoldContacts to refresh your contact list, including your new contact. As an aside, a future release of BoldContacts will add automatic refresh.
